/* sitefoot.js — pied de page commun à TOUTES les pages du sous-domaine market.
   Source unique (décision Elena, 2026-06-13) — pendant du siteheader.js.

   Génère l'intégralité du footer : présentation, liens « La méthode », liens
   « Marchés étudiés », mentions légales, + sélecteur de langue (FR/EN) à droite.

   Bilingue (décision Elena, 2026-06-15) : FR = URLs actuelles, EN = préfixe /en/.
   La langue est déduite de l'URL. Les liens internes sont construits relativement
   à la racine de langue (L) ; tant qu'une page n'a pas de version EN, son lien
   retombe sur le FR (EN_AVAILABLE ci-dessous — étendre à chaque vague de trad).

   ── Ajouter un marché : MARCHES (dans T.fr et T.en). Marquer une page traduite : EN_AVAILABLE. ──
*/
(function () {
  "use strict";

  var self = document.currentScript;
  var src = (self && self.getAttribute("src")) || "";
  var base = src.replace(/assets\/sitefoot\.js(?:[?#].*)?$/, "") || "./";

  // Langue (FR par défaut, EN sous /en/) + racine de langue relative.
  var path = location.pathname.replace(/\/[^/]*\.html$/, "/");
  var EN = /(^|\/)en(\/|$)/.test(path);
  var L = base + (EN ? "en/" : "");

  // Pages disposant déjà d'une version EN (chemins relatifs à la racine de langue).
  // Tant qu'un chemin n'y est pas, son lien pointe vers le FR même en page EN.
  var EN_AVAILABLE = { "": 1, "apps-perte-de-poids/": 1, "how-it-works/": 1 };

  function href(p) { return (EN && EN_AVAILABLE[p]) ? (L + p) : (base + p); }

  // « La méthode » — libellés identiques FR/EN (titres anglais déjà employés côté FR).
  var METHODE = [
    ["how-it-works/", "How it works"],
    ["how-it-works/focus-step-1/", "Focus on Step 1"],
    ["how-it-works/focus-step-2/", "Focus on Step 2"],
    ["how-it-works/focus-step-3/", "Focus on Step 3"],
    ["how-it-works/focus-step-4/", "Focus on Step 4"]
  ];

  var T = EN ? {
    method: "The method", markets: "Markets covered",
    about: "Market studies generated by an AI agent: players identified, shares estimated, " +
      "interactive positioning and strategic recommendations — a dated report each cycle.",
    legal: "© 2026 My Market Data — studies generated by AI; estimated figures, for information only.",
    host: "Hosted by OVH · ",
    marches: [["apps-perte-de-poids/", "Mobile weight-loss assistance apps"]]
  } : {
    method: "La méthode", markets: "Marchés étudiés",
    about: "Études de marché générées par un agent IA : acteurs, parts de marché, " +
      "positionnement interactif et recommandations stratégiques — un rapport daté à chaque cycle.",
    legal: "© 2026 My Market Data — études générées par IA, données estimées fournies à titre indicatif.",
    host: "Hébergé par OVH · ",
    marches: [["apps-perte-de-poids/", "Applications mobiles d'assistance à la perte de poids"]]
  };

  var STYLE_ID = "sitefoot-style";
  var CSS =
    ".sitefoot{margin-top:80px;background:#f4f1ea;border-top:1px solid #e7e2d8;" +
      "color:#494d59;font-family:'Hanken Grotesk',system-ui,-apple-system,'Segoe UI',sans-serif;" +
      "font-size:13.5px;line-height:1.5;}" +
    ".sitefoot-inner{max-width:calc(50vw + 550px);margin:0 auto;padding:40px 20px 28px;}" +
    ".sitefoot-top{display:flex;flex-wrap:wrap;gap:26px 48px;}" +
    ".sitefoot-about{flex:1 1 260px;min-width:230px;}" +
    ".sitefoot-brand{font-family:'Hanken Grotesk','Plus Jakarta Sans',system-ui,sans-serif;font-weight:800;" +
      "font-size:18px;letter-spacing:-.015em;color:#1a1c22;text-decoration:none;}" +
    ".sitefoot-brand .dot{color:#f4684f;}" +
    ".sitefoot-about p{margin:11px 0 0;max-width:40ch;color:#494d59;font-size:14px;}" +
    ".sitefoot-group{flex:0 0 auto;}" +
    ".sitefoot-h{display:block;font-family:'IBM Plex Mono',ui-monospace,monospace;font-size:11px;font-weight:600;" +
      "text-transform:uppercase;letter-spacing:.12em;color:#868a93;margin:0 0 13px;}" +
    ".sitefoot-group ul{list-style:none;margin:0;padding:0;}" +
    ".sitefoot-group li{margin:0 0 8px;}" +
    ".sitefoot-group a{color:#494d59;text-decoration:none;font-weight:500;font-size:14px;}" +
    ".sitefoot-group a:hover{color:#1a1c22;}" +
    ".sitefoot-legal{margin-top:28px;padding-top:18px;border-top:1px solid #e7e2d8;" +
      "display:flex;flex-wrap:wrap;justify-content:space-between;align-items:baseline;gap:10px 24px;" +
      "font-family:'IBM Plex Mono',ui-monospace,monospace;font-size:12px;color:#868a93;}" +
    ".sitefoot-legal a{color:#868a93;text-decoration:none;}" +
    ".sitefoot-legal a:hover{color:#1a1c22;}" +
    ".sitefoot-legal__txt{display:flex;flex-wrap:wrap;gap:4px 18px;}" +
    ".sitefoot-lang{display:inline-flex;align-items:baseline;gap:8px;white-space:nowrap;}" +
    ".sitefoot-lang a{color:#868a93;}" +
    ".sitefoot-lang .is-active{color:#1a1c22;font-weight:700;}" +
    ".sitefoot-lang .is-soon{opacity:.4;cursor:default;}" +
    ".sitefoot-lang .sep{opacity:.5;}" +
    "@media print{.sitefoot{display:none!important;}}" +
    "@media (max-width:560px){.sitefoot-inner{padding:30px 18px 24px;}" +
      ".sitefoot-top{gap:22px 32px;}.sitefoot-legal{flex-direction:column;}}";

  function injectStyle() {
    if (document.getElementById(STYLE_ID)) return;
    var s = document.createElement("style");
    s.id = STYLE_ID; s.textContent = CSS;
    (document.head || document.documentElement).appendChild(s);
  }

  function linkList(items) {
    var ul = document.createElement("ul");
    items.forEach(function (it) {
      var li = document.createElement("li");
      var a = document.createElement("a");
      a.href = href(it[0]);
      a.textContent = it[1];
      li.appendChild(a); ul.appendChild(li);
    });
    return ul;
  }

  function group(label, items) {
    var nav = document.createElement("nav");
    nav.className = "sitefoot-group";
    nav.setAttribute("aria-label", label);
    var h = document.createElement("span");
    h.className = "sitefoot-h"; h.textContent = label;
    nav.appendChild(h); nav.appendChild(linkList(items));
    return nav;
  }

  // Sélecteur FR/EN : même page dans l'autre langue (URL absolue, structure de prod /en/).
  function langSwitch() {
    var box = document.createElement("div");
    box.className = "sitefoot-lang";
    var siteRoot = new URL(base, location.href).pathname;     // racine du site, absolue (/, /site/, …)
    var pn = location.pathname.replace(/index\.html$/, "");
    var langRoot = siteRoot + (EN ? "en/" : "");
    var sub = pn.indexOf(langRoot) === 0 ? pn.slice(langRoot.length) : "";
    var enOk = EN || EN_AVAILABLE[sub] === 1;                  // version EN dispo pour cette page ?
    var items = [
      { label: "FR", url: siteRoot + sub, active: !EN, soon: false },
      { label: "EN", url: siteRoot + "en/" + sub, active: EN, soon: !EN && !enOk }
    ];
    items.forEach(function (it, i) {
      if (i) { var s = document.createElement("span"); s.className = "sep"; s.textContent = "·"; box.appendChild(s); }
      var el;
      if (it.active) { el = document.createElement("span"); el.className = "is-active"; }
      else if (it.soon) { el = document.createElement("span"); el.className = "is-soon"; el.title = "English version coming soon"; }
      else { el = document.createElement("a"); el.href = it.url; }
      el.textContent = it.label;
      el.setAttribute("hreflang", it.label.toLowerCase());
      box.appendChild(el);
    });
    return box;
  }

  function build() {
    var foot = document.createElement("footer");
    foot.className = "sitefoot"; foot.setAttribute("role", "contentinfo");
    var inner = document.createElement("div"); inner.className = "sitefoot-inner";

    var top = document.createElement("div"); top.className = "sitefoot-top";
    var about = document.createElement("div"); about.className = "sitefoot-about";
    var brand = document.createElement("a"); brand.className = "sitefoot-brand"; brand.href = L;
    brand.appendChild(document.createTextNode("My Market Data"));
    var dot = document.createElement("span"); dot.className = "dot"; dot.textContent = "."; brand.appendChild(dot);
    about.appendChild(brand);
    var p = document.createElement("p"); p.textContent = T.about; about.appendChild(p);
    top.appendChild(about);
    top.appendChild(group(T.method, METHODE));
    top.appendChild(group(T.markets, T.marches));
    inner.appendChild(top);

    var legal = document.createElement("div"); legal.className = "sitefoot-legal";
    var txt = document.createElement("div"); txt.className = "sitefoot-legal__txt";
    var l1 = document.createElement("span"); l1.textContent = T.legal;
    var l2 = document.createElement("span");
    l2.appendChild(document.createTextNode(T.host));
    var dom = document.createElement("a"); dom.href = L; dom.textContent = "market.shoette.com";
    l2.appendChild(dom);
    txt.appendChild(l1); txt.appendChild(l2);
    legal.appendChild(txt);
    legal.appendChild(langSwitch());
    inner.appendChild(legal);

    foot.appendChild(inner);
    return foot;
  }

  function inject() {
    if (document.querySelector(".sitefoot")) return;
    injectStyle();
    document.body.appendChild(build());
  }

  if (document.body) { inject(); } else { document.addEventListener("DOMContentLoaded", inject); }
})();
